//make a arrow function named is18 it takes one input, the function must return true if the given age is 18 else return false
// Define the arrow function is18
const is18 = (age) => age === 18;

// Example usage
console.log(is18(18)); // true
console.log(is18(17)); // false
console.log(is18(20)); // false
// console.log(is18(18.0)); // true, since 18 and 18.0 are considered equal in JavaScript
