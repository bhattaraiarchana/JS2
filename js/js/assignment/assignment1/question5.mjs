const averageOfThree = (num1, num2, num3) => (num1 + num2 + num3) / 3;

// Example usage
console.log(averageOfThree(4, 7, 10)); // 7
console.log(averageOfThree(1, 2, 3));  // 2
console.log(averageOfThree(10, 20, 30)); // 20
console.log(averageOfThree(-3, 0, 3)); // 0
console.log(averageOfThree(5, 5, 5)); // 5
