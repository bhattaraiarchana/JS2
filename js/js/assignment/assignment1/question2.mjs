const isGreaterThan18 = age => age >= 18;

// Example usage
console.log(isGreaterThan18(18)); // true
console.log(isGreaterThan18(17)); // false
console.log(isGreaterThan18(20)); // true
console.log(isGreaterThan18(10)); // false
// console.log(isGreaterThan18(18.0)); // true, since 18 and 18.0 are considered equal in JavaScript
